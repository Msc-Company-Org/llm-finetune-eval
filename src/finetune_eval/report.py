"""Render a before/after report (Markdown) from a results dict."""

from __future__ import annotations

from typing import Any


def _fmt(value: Any) -> str:
    if isinstance(value, dict):
        return ", ".join(f"{k} {v}" for k, v in value.items())
    if isinstance(value, float):
        return f"{value:.4f}"
    return str(value)


def render_markdown(results: dict[str, Any]) -> str:
    base = results["baseline"]
    cand = results["candidate"]
    base_m = base["metrics"]
    cand_m = cand["metrics"]

    metric_keys = list(dict.fromkeys([*base_m.keys(), *cand_m.keys()]))

    lines = [
        f"# Eval: {results['task']}",
        "",
        f"_{results['n_examples']} examples · baseline `{base['model']}` "
        f"vs. candidate `{cand['model']}`_",
        "",
        "| Metric | Baseline | Candidate |",
        "|---|---|---|",
    ]
    for key in metric_keys:
        lines.append(
            f"| {key} | {_fmt(base_m.get(key, '—'))} | {_fmt(cand_m.get(key, '—'))} |"
        )

    # A plain-language verdict on the headline accuracy metric, if present.
    for acc_key in ("json_match", "exact_match", "f1_token"):
        if acc_key in base_m and acc_key in cand_m:
            delta = cand_m[acc_key] - base_m[acc_key]
            verdict = "beats" if delta > 0 else "trails" if delta < 0 else "matches"
            lines += [
                "",
                f"**Verdict:** on `{acc_key}`, the candidate {verdict} the baseline "
                f"by {delta:+.4f}.",
            ]
            break

    lines += [
        "",
        "---",
        "_Generated by [llm-finetune-eval](https://github.com/Msc-Company-Org/llm-finetune-eval)."
        " A fine-tune without a before/after eval is a vibe, not an upgrade._",
        "",
    ]
    return "\n".join(lines)
